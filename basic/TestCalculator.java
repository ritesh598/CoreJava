package in.co.rays.basic;

public class TestCalculator {
public static void main(String[] args) {
	int a = 5;
	int b = 10;
	String opr = "-" ;
	switch ( opr) {
	case "+":
		System.out.println("result: " + (a+b));
		
		break;

	default:
		break;
	case "-":
		System.out.println("result: " + (a-b));
		
		break;
	case "*":
		System.out.println("result: " + (a*b));
		
		break;

	case "/":
		System.out.println("result: " + (a/b));
		
		break;
	case "%":
		System.out.println("result: " + (a%b));
		
		break;
	
		}
	}


	}
	


